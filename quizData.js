// =============== 題目輸入欄位初始化（由本檔建立） ===============
// 注意：本專案的 index.html 使用 #student-grade / #subject-name / #answers-form
// 以及 script.js 會在 DOMContentLoaded 時呼叫 initializeForm（若存在）。

const DEFAULT_GRADE = "國二學科聯合模擬會考挑戰";
const DEFAULT_SUBJECT = "國文";

let quizData = null;

/** 依目前 quizData.questions 產生答案輸入格（只產生必要 DOM，不改其他行為） */
function initializeForm() {
  const answersForm = document.getElementById('answers-form');
  if (!answersForm) return;

  answersForm.innerHTML = '';

  if (!quizData || !Array.isArray(quizData.questions) || quizData.questions.length === 0) {
    answersForm.innerHTML = '<p style="text-align:center;color:#888;">請先選擇科目</p>';
    return;
  }

  quizData.questions.forEach((q) => {
    const skillsText = Array.isArray(q.skill) ? q.skill.join(' / ') : String(q.skill || '');

    const group = document.createElement('div');
    group.className = 'question-group';

    const label = document.createElement('label');
    label.htmlFor = `q${q.id}`;
    label.textContent = `第 ${q.id} 題`;
    label.title = skillsText;

    const input = document.createElement('input');
    input.type = 'text';
    input.id = `q${q.id}`;
    input.maxLength = 1;
    input.placeholder = 'A～E';
    input.autocomplete = 'off';
    input.className = 'answer-input';

    group.appendChild(label);
    group.appendChild(input);
    answersForm.appendChild(group);
  });

  const firstInput = answersForm.querySelector('.answer-input');
  if (firstInput) firstInput.focus();
}

/** 填入科目選單（排除 gradeBands 設定物件） */
function populateSubjectsForGrade(gradeName, subjectSelect) {
  const allSubjectData = allGradeData?.[gradeName];
  subjectSelect.innerHTML = '<option value="">選擇科目</option>';

  if (!allSubjectData) return;

  Object.keys(allSubjectData).forEach((subject) => {
    if (subject === "gradeBands") return; // 重要：不要把級距設定當作科目
    const option = document.createElement('option');
    option.value = subject;
    option.textContent = subject;
    subjectSelect.appendChild(option);
  });
}

document.addEventListener('DOMContentLoaded', () => {
  const gradeSelect = document.getElementById('student-grade');
  const subjectSelect = document.getElementById('subject-name');

  if (!gradeSelect || !subjectSelect) return;

  // 年級/測驗下拉：顯示 allGradeData 的全部 key
  gradeSelect.innerHTML = '';
  Object.keys(allGradeData).forEach((g) => {
    const opt = document.createElement('option');
    opt.value = g;
    opt.textContent = g;
    gradeSelect.appendChild(opt);
  });

  // 先選 DEFAULT_GRADE；若不存在則選第一個
  const gradeKeys = Object.keys(allGradeData);
  const initGrade = allGradeData[DEFAULT_GRADE] ? DEFAULT_GRADE : (gradeKeys[0] || '');
  if (initGrade) gradeSelect.value = initGrade;

  // 依年級填入科目
  populateSubjectsForGrade(initGrade, subjectSelect);

  // 預設科目：先用 DEFAULT_SUBJECT，沒有就用第一個科目
  const initSubjects = Object.keys(allGradeData?.[initGrade] || {}).filter(k => k !== "gradeBands");
  const initSubject = (allGradeData?.[initGrade]?.[DEFAULT_SUBJECT]) ? DEFAULT_SUBJECT : (initSubjects[0] || '');
  if (initSubject) subjectSelect.value = initSubject;

  quizData = allGradeData?.[initGrade]?.[initSubject] || null;
  initializeForm();

  // 切年級：重載科目與題目
  gradeSelect.addEventListener('change', (e) => {
    const gradeName = e.target.value;
    populateSubjectsForGrade(gradeName, subjectSelect);

    subjectSelect.value = '';
    quizData = null;

    const answersForm = document.getElementById('answers-form');
    if (answersForm) answersForm.innerHTML = '<p style="text-align:center;color:#888;">請先選擇科目</p>';
  });

  // 切科目：載入題目
  subjectSelect.addEventListener('change', (e) => {
    const gradeName = gradeSelect.value;
    const selectedSubject = e.target.value;

    const allSubjectData = allGradeData?.[gradeName];
    if (!selectedSubject || !allSubjectData || !allSubjectData[selectedSubject]) {
      quizData = null;
      const answersForm = document.getElementById('answers-form');
      if (answersForm) answersForm.innerHTML = '<p style="text-align:center;color:#888;">請先選擇科目</p>';
      return;
    }

    quizData = allSubjectData[selectedSubject];
    initializeForm();
  });
});

